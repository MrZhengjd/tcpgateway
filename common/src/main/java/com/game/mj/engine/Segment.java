package com.game.mj.engine;

public class Segment {
    private Double start;
    private int leftCal;
    private Double end;
    private int rightCal;

    public Double getStart() {
        return start;
    }

    public void setStart(Double start) {
        this.start = start;
    }

    public int getLeftCal() {
        return leftCal;
    }

    public void setLeftCal(int leftCal) {
        this.leftCal = leftCal;
    }

    public Double getEnd() {
        return end;
    }

    public void setEnd(Double end) {
        this.end = end;
    }

    public int getRightCal() {
        return rightCal;
    }

    public void setRightCal(int rightCal) {
        this.rightCal = rightCal;
    }
}
